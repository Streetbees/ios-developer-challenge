import Foundation
import UIKit

extension UIImage {
    
    class func coverPlaceholder() -> UIImage {
        return UIImage(named: "cover-placeholder")!
    }
    
    class func iconBin() -> UIImage {
        return UIImage(named: "icon-bin")!
    }
    
    class func iconCamera() -> UIImage {
        return UIImage(named: "icon-camera")!
    }
    
}
