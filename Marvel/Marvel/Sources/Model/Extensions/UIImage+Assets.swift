import Foundation
import UIKit

extension UIImage {
    
    class func coverPlaceholder() -> UIImage {
        return UIImage(named: "cover-placeholder")!
    }
    
    class func iconBin() -> UIImage {
        return UIImage(named: "icon-bin")!
    }
    
    class func iconCamera() -> UIImage {
        return UIImage(named: "icon-camera")!
    }
    
    class func iconDropbox() -> UIImage {
        return UIImage(named: "icon-dropbox")!
    }
    
    class func bannerImage() -> UIImage {
        return UIImage(named: "banner-image")!
    }
    
    class func marvelLogo() -> UIImage {
        return UIImage(named: "marvel-logo")!
    }
    
}
